# Creating a list
a = [1,2,3,4]

# Printing  a list
print(a) 
print("\n") 

# Accessing a list using indices (a[0],a[1],a[-1] etc)
print(a[2])
print("\n") 

# Change the value of list using index
a[0] = 98
print(a)
print("\n") 

# We can create list of different types
b=['Trippi',29,29.33,True]
print(b)
print("\n") 

# List Slicing (As same as string slicing)
c = ['Trishan','divya','shivahs','durum']
print(c[0:2])
print("\n") 