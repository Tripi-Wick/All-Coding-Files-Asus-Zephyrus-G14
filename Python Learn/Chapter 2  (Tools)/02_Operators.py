# Comparison Operators (Return Boolean)
b=2
print(b<7)
print(b>7)
print(b>=1)
print(b<=7)
print(b==2)
print("\n")

# Logical Operators (and or not)
bool_1= True
bool_2= False

print("The value of bool 1 and Bool 2 for and is:",(bool_1 and bool_2))
print("The value of bool 1 and Bool 2 for or is:",(bool_1 or bool_2))
print("The value of bool 1 for not is:",(not bool_2) )
print("\n")

